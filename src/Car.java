public class Car {
    private int ID;
    private String brand;
    private String model;
    private String color;
    private int year;
    private double price;
    private boolean available;

    public Car() { }

    public int getID() {
        return ID;
    }

    public String getBrand() {
        return brand;
    }

    public String getColor() {
        return color;
    }

    public int getYear(){
        return year;
    }

    public String getModel()
    {
        return model;
    }
}
